export const getDataTree = (id, data) => {
  const child = data.filter(one => one.pid === id);
  return child.map(one => ({
    value: one.orgName,
    id: one.id,
    pid: one.pid,
    type: one.type,
    remarks: one.remarks,
    isManager: one.isManager,
    children: one.type ? this.getDataTree(one.id, data) : null
  }));
};

